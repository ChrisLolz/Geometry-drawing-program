/**
 * hasHeightAndWidth.Java 
 * Interface for shapes that need getters for a height and width
 * @author Christopher Lo
 * @version 1.0
 * May 12, 2021
 */

public interface hasHeightAndWidth {
  /**
   * getHeight
   * Getter method for the height of shapes that have a height
   */
  int getHeight();
  
  /**
   * getWidth
   * Getter method for the width of shapes that have a width
   */
  int getWidth();
}